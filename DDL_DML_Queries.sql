	WORKER_ID INT NOT NULL PRIMARY KEY IDENTITY(1,1),
	FIRST_NAME Varchar(25),
	LAST_NAME Varchar(25),
	SALARY INT,
	JOINING_DATE DATETIME,
	DEPARTMENT CHAR(25)
);

